CREATE VIEW [dbo].[vDepreciationRule]
AS
SELECT dbo.tDepreciationRule.idClient, dbo.tDepreciationRule.TitleDepreciationRule AS Title, dbo.tDepreciationRule.TypeDepreciationRule AS Type, AccountActivation.AccountCode AS [Account Activation], 
                  AccountDepreciation.AccountCode AS [Account Depreciation], dbo.tDepreciationRule.NumberOfPeriods AS Periods, dbo.tDepreciationRule.PeriodInMonths AS [Months per Period], dbo.tDepreciationRule.Id
FROM     dbo.tDepreciationRule INNER JOIN
                  dbo.tAccount AS AccountActivation ON dbo.tDepreciationRule.idAccountActivation = AccountActivation.Id INNER JOIN
                  dbo.tAccount AS AccountDepreciation ON dbo.tDepreciationRule.idAccountDepreciation = AccountDepreciation.Id
